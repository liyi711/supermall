<template>
	<swiper>
		<swiper-item v-for="(item, index) in banners" :key="index">
			<a :href="item.link">
				<img :src="item.image" @load="swiperImageLoad" />
			</a>
		</swiper-item>
	</swiper>
</template>

<script>
import { Swiper, SwiperItem } from 'components/common/swiper/index.js'
export default {
	name: 'HomeNavBar',
	data() {
		return {
			//节流阀
			isLoad: false,
		}
	},
	props: {
		banners: {
			type: Array,
			default() {
				return {}
			},
		},
	},
	components: {
		Swiper,
		SwiperItem,
	},
	methods: {
		swiperImageLoad() {
			if (!this.isLoad) {
				this.$emit('swiperImageLoad')
				this.isLoad = true
			}
		},
	},
}
</script>

